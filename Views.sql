/* Βήμα Α: Δημιουργία της όψης */
CREATE VIEW ΠλήθοςΕικονιδίωνΑνάΕπιφάνεια AS
SELECT ΌνομαΕπιφάνειας, COUNT(*) AS ΠλήθοςΕικονιδίων
FROM ΕΙΚΟΝΙΔΙΟ
GROUP BY ΌνομαΕπιφάνειας;

/* Βήμα Β: Τελικό ερώτημα */
SELECT ΕΠ.ΌνομαΕπιφάνειας, ΕΠ.ΗμερομηνίαΔημιουργίας, ΠλΕικ.ΠλήθοςΕικονιδίων
FROM ΕΠΙΦΑΝΕΙΑ_ΕΡΓΑΣΙΑΣ ΕΠ
JOIN ΠλήθοςΕικονιδίωνΑνάΕπιφάνεια ΠλΕικ ON ΕΠ.ΌνομαΕπιφάνειας = ΠλΕικ.ΌνομαΕπιφάνειας
WHERE ΠλΕικ.ΠλήθοςΕικονιδίων > 18
ORDER BY ΠλΕικ.ΠλήθοςΕικονιδίων DESC;

/*2*/
/* Βήμα Α: Πρώτη όψη - Επιφάνειες ανά χρήστη */
CREATE VIEW "ΕπιφάνειεςΧρήστη" AS
SELECT "ΚωδικόςΧρήστη", COUNT(*) AS "ΠλήθοςΕπιφανειώνΧρήστη"
FROM ΕΠΙΦΑΝΕΙΑ_ΕΡΓΑΣΙΑΣ
GROUP BY "ΚωδικόςΧρήστη";

/* Βήμα Β: Δεύτερη όψη - Ρυθμίσεις ανά χρήστη */
CREATE VIEW "ΡυθμίσειςΧρήστη" AS
SELECT "ΚωδικόςΧρήστη", COUNT(*) AS "ΠλήθοςΡυθμίσεωνΧρήστη"
FROM ΕΠΙΛΕΓΕΙ
GROUP BY "ΚωδικόςΧρήστη";

/* Βήμα Γ: Τελικό ερώτημα με χρήση των δύο όψεων */
SELECT Χ."ΚωδικόςΧρήστη", Χ."ΌνομαΧρήστη", 
       ΕΠ2."ΠλήθοςΕπιφανειώνΧρήστη", 
       Ρ2."ΠλήθοςΡυθμίσεωνΧρήστη"
FROM ΧΡΗΣΤΗΣ Χ
JOIN ΕπιφάνειεςΧρήστη ΕΠ2 ON Χ."ΚωδικόςΧρήστη" = ΕΠ2."ΚωδικόςΧρήστη"
JOIN ΡυθμίσειςΧρήστη Ρ2 ON Χ."ΚωδικόςΧρήστη" = Ρ2."ΚωδικόςΧρήστη";

/*3*/
/* Βήμα Α: Πρώτη όψη - Ενέργειες ανά επιφάνεια εργασίας */
CREATE VIEW ΕνέργειεςΑνάΕπιφάνεια AS
SELECT ΌνομαΕπιφάνειας, COUNT(*) AS ΠλήθοςΕνεργειώνΕπιφάνειας
FROM ΕΝΕΡΓΕΙΑ
GROUP BY ΌνομαΕπιφάνειας;

/* Βήμα Β: Δεύτερη όψη - Συνολικές ενέργειες ανά χρήστη (χρησιμοποιεί την πρώτη) */
CREATE VIEW ΕνέργειεςΧρήστη AS
SELECT ΕΠ.ΚωδικόςΧρήστη, SUM(ΕΝΕ.ΠλήθοςΕνεργειώνΕπιφάνειας) AS "Πλήθος ενεργειών"
FROM ΕΠΙΦΑΝΕΙΑ_ΕΡΓΑΣΙΑΣ ΕΠ
JOIN ΕνέργειεςΑνάΕπιφάνεια ΕΝΕ ON ΕΠ.ΌνομαΕπιφάνειας = ΕΝΕ.ΌνομαΕπιφάνειας
GROUP BY ΕΠ.ΚωδικόςΧρήστη;

/* Βήμα Γ: Τελικό ερώτημα (εύρεση του/των χρήστη/ών με το MAX) */
SELECT Χ.ΚωδικόςΧρήστη, Χ.ΌνομαΧρήστη, ΕΝΧ."Πλήθος ενεργειών"
FROM ΧΡΗΣΤΗΣ Χ
JOIN ΕνέργειεςΧρήστη ΕΝΧ ON Χ.ΚωδικόςΧρήστη = ΕΝΧ.ΚωδικόςΧρήστη
WHERE ΕΝΧ."Πλήθος ενεργειών" = (SELECT MAX("Πλήθος ενεργειών") FROM ΕνέργειεςΧρήστη);